    private void AceptarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AceptarButtonActionPerformed
        // TODO add your handling code here:
        Conexion objConexion = new Conexion();
        if(objInventario != null){
            String cantS = JOptionPane.showInputDialog(null,"¿Cantidad a dar de baja?","Baja",JOptionPane.INFORMATION_MESSAGE);
            if(cantS != null){
                if(cantS.equals(""))
                    JOptionPane.showMessageDialog(null, "Inserte un valor no nulo","Error",JOptionPane.ERROR_MESSAGE);
                else{
                    if(cantS.matches("\\d+")){
                        int cant = Integer.parseInt(cantS);
                        if(cant > objInventario.getStock())
                            JOptionPane.showMessageDialog(null, "La cantidad no debe superar al stock","Error",JOptionPane.ERROR_MESSAGE);
                        else{
                            String strSentenciaInsert = String.format("UPDATE Inventario SET Stock=%d,Ventas_Dia=%d WHERE Codigo_barras=%d", 
                                objInventario.getStock()-cant, objInventario.getVentasDia()+cant, objInventario.getCodigo_barras());
                            if(objConexion.ejecutarSentenciaSQL(strSentenciaInsert) == 1)
                                JOptionPane.showMessageDialog(null, "Venta realizada con éxito", "Ventas", JOptionPane.INFORMATION_MESSAGE);
                            else
                                JOptionPane.showMessageDialog(null, "Error al realizar la venta", "Ventas", JOptionPane.ERROR_MESSAGE);                        }
                    }else
                        JOptionPane.showMessageDialog(null, "Inserte un valor numérico","Error",JOptionPane.ERROR_MESSAGE);
                }
            }
            objInventario = null;
        }
        objConexion.close();
        mostrarDatos();
    }//GEN-LAST:event_AceptarButtonActionPerformed

