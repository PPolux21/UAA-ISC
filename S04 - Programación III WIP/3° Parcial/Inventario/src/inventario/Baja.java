    private void tablaVentasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaVentasMouseClicked
        // TODO add your handling code here:
        objInventario = new Data_information();
        if(evt.getClickCount()==1){
            JTable receptor = (JTable)evt.getSource();
            String auxCodBarras = receptor.getModel().getValueAt(receptor.getSelectedRow(), 0).toString();
            objInventario.setCodigo_barras(Long.parseLong(auxCodBarras));
            String auxStock = receptor.getModel().getValueAt(receptor.getSelectedRow(), 5).toString();
            objInventario.setStock(Integer.parseInt(auxStock));
            String auxVentasDia = receptor.getModel().getValueAt(receptor.getSelectedRow(), 7).toString();
            objInventario.setVentasDia(Integer.parseInt(auxVentasDia));
        }
    }//GEN-LAST:event_tablaVentasMouseClicked

