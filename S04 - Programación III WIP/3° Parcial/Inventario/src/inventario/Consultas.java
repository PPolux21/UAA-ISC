    private void codigoBotonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_codigoBotonActionPerformed
        // TODO add your handling code here:
        //evalua si el codigo de barras es numerico
        Conexion objConexion = new Conexion();
        String strSentenciaInsert = String.format("SELECT * FROM Inventario WHERE Codigo_Barras=%s",codigo_Field.getText());
        if(objConexion.ejecutarSentenciaSQL(strSentenciaInsert) == 1){
            while(modelo.getRowCount()>0)
                modelo.removeRow(0);
            try{
                ResultSet resultado=objConexion.consultarRegistro("SELECT * FROM Inventario WHERE Codigo_Barras="+codigo_Field.getText());
                while(resultado.next()){
                    Object[] objUsuario = {resultado.getString("Codigo_Barras"), resultado.getString("Nombre"), 
                        resultado.getString("Precio"), resultado.getString("Marca"), resultado.getString("Fecha_Ingreso"), 
                        resultado.getString("Stock"), resultado.getString("Tipo"), resultado.getString("Ventas_Dia"), resultado.getString("Ventas_Mes")};
                    modelo.addRow(objUsuario);
                }
            }catch(Exception e){
                System.out.println("Error de consulta "+e);
            }
        }else
            JOptionPane.showMessageDialog(null, "El campo debe de contener numeros", "Error", JOptionPane.ERROR_MESSAGE);
        objConexion.close();
    }//GEN-LAST:event_codigoBotonActionPerformed

