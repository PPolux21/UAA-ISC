    private void costosBotonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_costosBotonActionPerformed
        // TODO add your handling code here:
        Conexion objConexion = new Conexion();
        String strSentenciaInsert;
        if(ordenarPor.getSelectedItem().equals("Mayor"))
            strSentenciaInsert = String.format("SELECT * FROM Inventario WHERE Precio BETWEEN %s AND %s ORDER BY Precio",primerValor.getValue(),segundoValor.getValue());
        else
            strSentenciaInsert = String.format("SELECT * FROM Inventario WHERE Precio BETWEEN %s AND %s ORDER BY Precio DESC",primerValor.getValue(),segundoValor.getValue());
        objConexion.ejecutarSentenciaSQL(strSentenciaInsert);
        while(modelo.getRowCount()>0)
            modelo.removeRow(0);
        try{
            ResultSet resultado=null;
            if(ordenarPor.getSelectedItem().equals("Mayor"))
                resultado=objConexion.consultarRegistro("SELECT * FROM Inventario WHERE Precio BETWEEN "+primerValor.getValue()+" AND "+segundoValor.getValue()+" ORDER BY Precio");
            else
                resultado=objConexion.consultarRegistro("SELECT * FROM Inventario WHERE Precio BETWEEN "+primerValor.getValue()+" AND "+segundoValor.getValue()+" ORDER BY Precio DESC");
            while(resultado.next()){
                Object[] objUsuario = {resultado.getString("Codigo_Barras"), resultado.getString("Nombre"), 
                    resultado.getString("Precio"), resultado.getString("Marca"), resultado.getString("Fecha_Ingreso"), 
                    resultado.getString("Stock"), resultado.getString("Tipo"), resultado.getString("Ventas_Dia"), resultado.getString("Ventas_Mes")};
                modelo.addRow(objUsuario);
            }
        }catch(Exception e){
            System.out.println("Error de consulta "+e);
        }
        objConexion.close();
    }//GEN-LAST:event_costosBotonActionPerformed

