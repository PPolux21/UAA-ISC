    @Override
    public Data_information recuperarDatos(){
        Data_information objInventarios = new Data_information();
        String errMessage="";
        //sacar el codigo de barras
        if(Codigo_Field.getText().isEmpty() || !Codigo_Field.getText().trim().matches("\\d{1,9}"))
            errMessage += " 'Codigo de barras' ";
        else
            objInventarios.setCodigo_barras(Long.parseLong(Codigo_Field.getText()));
        //sacar el nombre
        if(Nombre_Field.getText().isEmpty() || !Nombre_Field.getText().trim().matches("[\\w\\s]{1,100}"))
            errMessage += " 'Nombre' ";
        else
            objInventarios.setNombre(Nombre_Field.getText());
        //sacar el precio
        if(Precio_Field.getText().isEmpty() || !Precio_Field.getText().trim().matches("\\d+(\\.\\d{1,2})?"))
            errMessage += " 'Precio' ";
        else
            objInventarios.setPrecio(Double.parseDouble(Precio_Field.getText()));
        //sacar la marca
        if(Marca_Field.getText().isEmpty() || !Marca_Field.getText().trim().matches("[\\w\\s]{1,50}"))
            errMessage += " 'Marca' ";
        else
            objInventarios.setMarca(Marca_Field.getText());
        //sacar la cantidad/stock
        if(Cant_Field.getText().isEmpty() || !Cant_Field.getText().trim().matches("\\d+"))
            errMessage += " 'Cantidad' ";
        else
            objInventarios.setStock(Integer.parseInt(Cant_Field.getText()));
        //sacar el tipo
        objInventarios.setTipo(tipoBox.getSelectedItem().toString());
        
        //sacar el descuento
        objInventarios.setDescuento(Integer.parseInt(jComboBox1.getSelectedItem().toString()));
        
        if(!errMessage.equals("")){
            JOptionPane.showMessageDialog(null,"Los campos"+errMessage+"se encuentran vacios o son erroneos","Error",JOptionPane.ERROR_MESSAGE);
            error=true;
        }
        return objInventarios;
    }
    
