    private void AceptarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AceptarButtonActionPerformed
        // TODO add your handling code here:
        Conexion objConexion = new Conexion();
        Data_information objInventario = recuperarDatos();
        if(!error){
            calcula();
            String strSentenciaInsert = String.format("INSERT INTO Inventario (Codigo_Barras,Nombre,Precio,Marca,Fecha_Ingreso,Stock,Tipo,Ventas_Dia,Ventas_Mes,Descuento) "
                + "VALUES ('%d','%s','%.2f','%s','%s','%d','%s','%d','%d','%d')", 
                objInventario.getCodigo_barras(), objInventario.getNombre(), objInventario.getPrecio(), 
                objInventario.getMarca(), fecha, objInventario.getStock(), objInventario.getTipo(), 0, 0, objInventario.getDescuento());
            if(objConexion.ejecutarSentenciaSQL(strSentenciaInsert) == 1)
                JOptionPane.showMessageDialog(null,"Los datos se han insertado correctamente","Insercion",JOptionPane.INFORMATION_MESSAGE);
            else
                JOptionPane.showMessageDialog(null,"Error al insertar los datos","Insercion",JOptionPane.ERROR_MESSAGE);
        }
        objConexion.close();
        error = false;
    }//GEN-LAST:event_AceptarButtonActionPerformed

