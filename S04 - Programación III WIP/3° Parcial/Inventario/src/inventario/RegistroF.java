    private void calcula(){
        Calendar calendario = new GregorianCalendar();
        Date fechaActual = new Date();
        calendario.setTime(fechaActual);
        dia = calendario.get(Calendar.DAY_OF_MONTH);
        mes = calendario.get(Calendar.MONTH) + 1;       //el mes marca un numero menor
        anio = calendario.get(Calendar.YEAR);
        if(dia < 10){
            if(mes < 10)
                fecha = String.format("%d-0%d-0%d", anio,mes,dia);  //si el dia y el mes son menores que 10
            else
                fecha = String.format("%d-%d-0%d", anio,mes,dia);       //si SOLO el dia es menor que 10
        }
        else{
            if(mes < 10)
                fecha = String.format("%d-0%d-%d", anio,mes,dia);  //si SOLO el mes es menor que 10
            else
                fecha = String.format("%d-%d-%d", anio,mes,dia);       //si NINGUNO es menor que 10
        }
    }
    
